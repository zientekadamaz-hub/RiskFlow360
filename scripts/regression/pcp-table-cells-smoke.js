const assert = require('node:assert/strict')
const fs = require('node:fs')
const path = require('node:path')

const root = path.join(__dirname, '..', '..')
const pageSource = fs.readFileSync(path.join(root, 'app', 'pcp', 'page.tsx'), 'utf8')
const cellsSource = fs.readFileSync(path.join(root, 'src', 'features', 'pcp', 'pcp-table-cells.tsx'), 'utf8')
const tableSource = fs.readFileSync(path.join(root, 'src', 'features', 'pcp', 'pcp-table.tsx'), 'utf8')

assert.match(cellsSource, /export function SummaryCard/, 'PCP table cells must export SummaryCard.')
assert.match(cellsSource, /export function Th/, 'PCP table cells must export table header cell.')
assert.match(cellsSource, /borderBottom: '1px solid rgba\(255,255,255,0\.2\)'/, 'PCP header must use PFMEA-style bottom border.')
assert.match(cellsSource, /borderRight: '1px solid rgba\(255,255,255,0\.14\)'/, 'PCP header must use PFMEA-style right border.')
assert.match(cellsSource, /padding: '10px 8px'/, 'PCP header must keep PFMEA-like header height with tighter horizontal padding.')
assert.doesNotMatch(cellsSource, /border: '1px solid rgba\(255,255,255,0\.14\)'/, 'PCP header must avoid full-cell borders that thicken the table frame.')
assert.match(cellsSource, /export function TdRead/, 'PCP table cells must export read-only cell.')
assert.match(cellsSource, /export function TdText/, 'PCP table cells must export editable text cell.')
assert.match(cellsSource, /export function TdClassPopup/, 'PCP table cells must export class popup cell.')
assert.match(cellsSource, /export function TdSelectPopup/, 'PCP table cells must export select popup cell.')
assert.match(cellsSource, /createPortal/, 'PCP class popup must keep portal rendering.')
assert.match(cellsSource, /CLASS_OPTION_DETAILS/, 'PCP class popup must keep SC\/CC descriptions.')
assert.match(cellsSource, /PCP_CLASS_OPTIONS/, 'PCP class popup must keep class option list.')
assert.match(cellsSource, /function PcpTextEditor/, 'PCP text cell must keep local draft state inside editor-only component.')
assert.match(cellsSource, /const commitIfChanged = \(\) => \{[\s\S]*props\.onCancel\(\)[\s\S]*props\.onCommit\(val\)/, 'PCP text cell must keep blur commit behavior.')
assert.match(cellsSource, /onBlur=\{commitIfChanged\}/, 'PCP editor must commit on blur.')

assert.match(pageSource, /SettingsPageShell/, 'PCP page must use the shared settings page shell for the top frame.')
assert.match(pageSource, /<SettingsSummaryGrid columns=\{3\} maxWidth=\{390\}>/, 'PCP page must keep the standard compact summary grid.')
assert.match(pageSource, /<SettingsSummaryTile label="Process"/, 'PCP page must keep the standard process summary tile.')
assert.doesNotMatch(pageSource, /<SummaryCard/, 'PCP page must not render custom summary cards in the standard header.')
assert.doesNotMatch(pageSource, /function TdText/, 'PCP page should not define TdText inline.')
assert.doesNotMatch(pageSource, /function TdClassPopup/, 'PCP page should not define TdClassPopup inline.')
assert.doesNotMatch(pageSource, /createPortal/, 'PCP page should not import portal directly after extraction.')
assert.match(tableSource, /<TdClassPopup/, 'PCP table must still render class popup cells.')
assert.match(tableSource, /<TdText/, 'PCP table must still render editable text cells.')

console.log('pcp table cells smoke passed')
